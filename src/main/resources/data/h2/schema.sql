DROP TABLE CO_PART IF EXISTS;
DROP TABLE C_PERSON IF EXISTS;
DROP TABLE C_PERSON_ROLE IF EXISTS;
DROP TABLE H_SUBJECT IF EXISTS;
DROP TABLE SS_TASK IF EXISTS;

CREATE TABLE CO_PART (
  ID bigint(20) NOT NULL PRIMARY KEY AUTO_INCREMENT,
  NAME varchar(255) NOT NULL,
  LOCATION varchar(255) NOT NULL,
  SIZE bigint(20) DEFAULT NULL,
  WHATSTYPE varchar(255) NOT NULL,
);

CREATE TABLE C_PERSON (
  ID bigint(16) NOT NULL PRIMARY KEY AUTO_INCREMENT,
  SSOID varchar(16) DEFAULT NULL,
  STATUS varchar(4) DEFAULT NULL,
  LOGIN_NAME varchar(64) DEFAULT NULL,
  LOGIN_PASSWORD varchar(64) DEFAULT NULL,
  USER_NAME varchar(64) DEFAULT NULL,
  CREATE_TIME varchar(16) DEFAULT NULL,
  MODIFY_TIME varchar(16) DEFAULT NULL,
  DESCRIPTION varchar(1024) DEFAULT NULL,
  ROLE_STATUS varchar(1) DEFAULT NULL,
  SECURITY_LEVEL char(1) DEFAULT '0',
  EMAIL varchar(255) DEFAULT NULL,
);

CREATE TABLE C_PERSON_ROLE (
  ID bigint(16) NOT NULL PRIMARY KEY AUTO_INCREMENT,
  PERSON_ID varchar(16) DEFAULT NULL,
  ROLE varchar(1) DEFAULT NULL,
  DEPT_ID bigint(20) DEFAULT NULL,
  DEPT_DN varchar(512) DEFAULT NULL,
  INCLUDE_SON varchar(1) DEFAULT NULL,
  SECURITY_LEVEL varchar(1) DEFAULT NULL,
  POS int(16) DEFAULT NULL,
  ROLE_NAME varchar(255) DEFAULT NULL,
);

CREATE TABLE SS_TASK (
  ID bigint(16) NOT NULL PRIMARY KEY AUTO_INCREMENT,
  TITLE varchar(255) DEFAULT NULL,
  DESCRIPTION varchar(255) DEFAULT NULL,
  PERSON_ID bigint(16) DEFAULT NULL,
);